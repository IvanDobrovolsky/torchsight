#!/usr/bin/env node
// Reads VERSION from repo root and stamps it into:
// - site/src/version.ts (for Astro pages)
// - site/package.json
// - desktop/package.json
// - desktop/src-tauri/Cargo.toml
// - desktop/src-tauri/tauri.conf.json
// - Cargo.toml (workspace)

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..', '..');

const version = readFileSync(join(root, 'VERSION'), 'utf-8').trim();
console.log(`[sync-version] ${version}`);

// 1. site/src/version.ts
writeFileSync(
  join(root, 'site', 'src', 'version.ts'),
  `// Auto-generated by scripts/sync-version.mjs — do not edit\nexport const VERSION = 'v${version}';\n`
);

// 2. package.json files
for (const rel of ['site/package.json', 'desktop/package.json']) {
  const path = join(root, rel);
  try {
    const pkg = JSON.parse(readFileSync(path, 'utf-8'));
    pkg.version = version;
    writeFileSync(path, JSON.stringify(pkg, null, 2) + '\n');
  } catch {}
}

// 3. desktop/src-tauri/tauri.conf.json
try {
  const tauriPath = join(root, 'desktop', 'src-tauri', 'tauri.conf.json');
  const tauri = JSON.parse(readFileSync(tauriPath, 'utf-8'));
  tauri.version = version;
  writeFileSync(tauriPath, JSON.stringify(tauri, null, 2) + '\n');
} catch {}

// 4. Cargo.toml workspace version
for (const rel of ['Cargo.toml', 'desktop/src-tauri/Cargo.toml']) {
  const path = join(root, rel);
  try {
    let content = readFileSync(path, 'utf-8');
    content = content.replace(/^version\s*=\s*"[^"]*"/m, `version = "${version}"`);
    writeFileSync(path, content);
  } catch {}
}
